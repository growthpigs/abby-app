// =============================================================================
// TYPE DEFINITIONS
// =============================================================================

export type Priority = 'P0' | 'P1' | 'P2' | 'P3';
// P0 = Dealbreakers (must match or no match possible)
// P1 = Core compatibility (Gottman pillars, attachment)
// P2 = Lifestyle alignment (daily life compatibility)
// P3 = Preferences (nice-to-have, enrichment)

export type AnswerType =
  | 'binary'           // yes/no
  | 'scale_1_5'        // 1-5 rating
  | 'scale_1_10'       // 1-10 rating
  | 'single_choice'    // pick one from options
  | 'multi_choice'     // pick multiple from options
  | 'free_text'        // open-ended (for semantic extraction)
  | 'numeric'          // specific number (age, etc.)
  | 'date';            // date value

export type VibeShift = 'TRUST' | 'PASSION' | 'CAUTION' | 'GROWTH' | 'DEEP' | 'ALERT' | null;

export type Category =
  | 'dealbreakers'
  | 'values_beliefs'
  | 'relationship_style'
  | 'communication'
  | 'conflict_resolution'
  | 'intimacy_physical'
  | 'intimacy_emotional'
  | 'lifestyle'
  | 'family_future'
  | 'career_ambition'
  | 'finances'
  | 'personality'
  | 'interests_hobbies'
  | 'social_life'
  | 'health_wellness';

export interface DataPoint {
  id: string;                    // Unique identifier (e.g., "DB_001")
  category: Category;
  priority: Priority;
  question: string;              // The actual question text
  answer_type: AnswerType;
  options?: string[];            // For choice-based questions
  vibe_shift?: VibeShift;        // Visual state change on answer
  semantic_keys: string[];       // Keys this question can satisfy via conversation
  follow_up_ids?: string[];      // Questions to ask next based on answer
  gottman_dimension?: string;    // Maps to Gottman's research
  big_five_trait?: string;       // Maps to OCEAN personality
  weight: number;                // 1-10, importance for matching algorithm
}

