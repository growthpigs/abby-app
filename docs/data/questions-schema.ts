// HELPER FUNCTIONS
// =============================================================================

export function getByPriority(priority: Priority): DataPoint[] {
  return ALL_DATA_POINTS.filter(dp => dp.priority === priority);
}

export function getByCategory(category: Category): DataPoint[] {
  return ALL_DATA_POINTS.filter(dp => dp.category === category);
}

export function getById(id: string): DataPoint | undefined {
  return ALL_DATA_POINTS.find(dp => dp.id === id);
}

export function getBySemanticKey(key: string): DataPoint[] {
  const lowerKey = key.toLowerCase();
  return ALL_DATA_POINTS.filter(dp =>
    dp.semantic_keys.some(sk => sk.toLowerCase().includes(lowerKey))
  );
}

export function getUnanswered(answeredIds: string[]): DataPoint[] {
  return ALL_DATA_POINTS.filter(dp => !answeredIds.includes(dp.id));
}

export function getNextPriorityQuestion(answeredIds: string[]): DataPoint | null {
  const unanswered = getUnanswered(answeredIds);
  // Return highest priority (P0 first) with highest weight
  const sorted = unanswered.sort((a, b) => {
    const priorityOrder = { P0: 0, P1: 1, P2: 2, P3: 3 };
    if (priorityOrder[a.priority] !== priorityOrder[b.priority]) {
      return priorityOrder[a.priority] - priorityOrder[b.priority];
    }
    return b.weight - a.weight;
  });
  return sorted[0] || null;
}

